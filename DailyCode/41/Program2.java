// Interface Multiple Inheritance

interface AInter {
          int x = 10; 
          void fun();

}
interface BInter {

          void run();

}
class Demo implements AInter,BInter {

	public void fun(){
	System.out.println("In fun");
	System.out.println(x);
	}
	public void run() {
        System.out.println("In run");
	
	
	}
}
class Client {
     public static void main(String[] args){
     
	     Demo obj = new Demo();
	     obj.fun();
	     obj.run();
     }
    
     
     
}
