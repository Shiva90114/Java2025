// Relation Opertion

class Relation {

	public static void main(String[] args) {
	
		int x =10;
		int y =15;
		System.out.println("x =10 and y =15 ");
		System.out.println("x > y = "+(x>y));
		System.out.println("x >= y = "+(x>=y));
		System.out.println("x <= y = "+(x<=y));
		System.out.println("x < y = "+(x<y));
		System.out.println("x != y = "+ (x!=y));
		System.out.println("x == y = "+ (x==y));
	}
}

